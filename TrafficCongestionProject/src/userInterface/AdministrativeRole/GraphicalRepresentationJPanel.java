    private void btnCongestionRouteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCongestionRouteActionPerformed
        // TODO add your handling code here:
        DefaultCategoryDataset chartData = new DefaultCategoryDataset();
        for (Route route : temporaryRouteList.getRouteList())
        {
            chartData.setValue(route.getProbCount(), "Number of Congestion Problems per Route", route.getRouteName());
        }

        JFreeChart barChart =ChartFactory.createBarChart("Number of Congestion Problems per Route", "Route", "Problem Occurence", chartData, PlotOrientation.VERTICAL,false, true, false);
        CategoryPlot chart = barChart.getCategoryPlot();
        
        chart.setRangeGridlinePaint(Color.GREEN);

        ChartPanel barPanel=new ChartPanel(barChart);
//        jPanel1.removeAll();
//        jPanel1.setVisible(true);
//        jPanel1.add(barPanel,BorderLayout.CENTER);
//        jPanel1.validate();
        
        ChartFrame frame=new ChartFrame("Bar chart for Problems per Route", barChart);
        frame.setVisible(true);
        frame.setSize(450, 350);

    }//GEN-LAST:event_btnCongestionRouteActionPerformed

